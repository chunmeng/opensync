config_name=intfstats
report_name=intf_stat_report

check_cmd() {
    cmd=$1
    path_cmd=$(which ${cmd})
    if [ -z ${path_cmd} ]; then
        echo "Error: could not find ${cmd} command in path"
        exit 1
    fi
    echo "found ${cmd} as ${path_cmd}"
}

del_Wifi_Inet_Config_cmd() {
    for i in $intf_list
    do
        ovsh u Wifi_Inet_Config -w if_name=="$i" collect_stats:=false
    done
}

# Check required commands
check_cmd 'ovsh'

# Wifi_Inet_Config parameters
intf_list="eth0 home-ap-24 home-ap-50 bhaul-ap-24 bhaul-ap-50"

ovsh d FCM_Collector_Config -w name==${config_name}
ovsh d FCM_Report_Config -w name==${report_name}

del_Wifi_Inet_Config_cmd
